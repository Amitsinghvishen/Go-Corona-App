package com.vishen.gocorona;

public class Data {
    String country;
    String cases;
    String deaths;
    String world;
    String totalDeadths;
    public String getDeaths() {
        return deaths;
    }

    public void setDeaths(String deaths) {
        this.deaths = deaths;
    }

    public String getWorld() {
        return world;
    }

    public void setWorld(String world) {
        this.world = world;
    }

    public String getTotalDeadths() {
        return totalDeadths;
    }

    public void setTotalDeadths(String totalDeadths) {
        this.totalDeadths = totalDeadths;
    }


    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCases() {
        return cases;
    }

    public void setCases(String cases) {
        this.cases = cases;
    }


}
